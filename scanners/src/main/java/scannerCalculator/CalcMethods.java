package scannerCalculator;

public class CalcMethods {

	public void addition(double a, double b, double ans) {
		ans = a + b;
		System.out.println(a + " + " + b + " = " + ans);
	}
	
	public void subtraction(double a, double b, double ans) {
		ans = a - b;
		System.out.println(ans);
	}
	
	public void multiply(double a, double b, double ans) {
		ans = a * b;
		System.out.println(ans);
	}
	
	public void divide(double a, double b, double ans) {
		ans = a / b;
		System.out.println(ans);
	}
}
